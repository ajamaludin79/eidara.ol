<script>
$(".process").click(function(){
	  $('#loading').fadeIn();	  
	  var name = $('#fullname').val(), msg = $('#message').val(), email = $('#email').val(), subject = $('#subject').val();
	  $.ajax({
		  type: 'POST',
		  url: '<?php echo base_url('index.php/welcome/send_notification_email');?>',
		  data:{name:name,msg:msg,email:email,subject:subject},
		  success: function(a){ 
		  //console.log(a);
		  var a = JSON.parse(a); 
			  if(a.status==1){				  
					/* $.each(a, function( index, value ) {			  
					  $( "."+index ).html(value);			 					  
					});  */
					$('.mess_errors').html(a.msg);
					$('.mess_errors').fadeIn();
					setTimeout(function(){$('.mess_errors').fadeOut();}, 3000);	
				}else{	
					$('.mess_errors').html('Your email has been sent<br/>Thank you.').fadeIn();
					setTimeout(function(){$('.mess_errors').fadeOut();}, 3000);							
					$('#fullname').val('');$('#message').val('');$('#email').val('');$('#subject').val('');
				}
			  $('#loading').fadeOut();
		  }
	  })			
});
</script>